<script setup>
import { ref, watch, onMounted } from 'vue'
import Dropdown from '@/Components/Dropdown.vue'

const volume = ref(0.7)

onMounted(() => {
  volume.value = localStorage.getItem('volume') ?? 0.7
  dispatch()
})

watch(volume, (value) => {
  localStorage.setItem('volume', value)
  dispatch()
})

const dispatch = () => {
  window.dispatchEvent(
    new CustomEvent('volume-localstorage-changed', {
      detail: {
        volume: volume.value,
      },
    }),
  )
}
</script>
<template>
  <Dropdown placement="bottom-end" :autoClose="false">
    <template #default>
      <div class="group mr-1 flex h-full cursor-pointer select-none items-center transition-colors hover:text-purple-500">
        <svg xmlns="http://www.w3.org/2000/svg" fill="none" viewBox="0 0 24 24" stroke-width="1.5" stroke="currentColor" class="h-6 w-6">
          <path stroke-linecap="round" stroke-linejoin="round" d="M19.114 5.636a9 9 0 010 12.728M16.463 8.288a5.25 5.25 0 010 7.424M6.75 8.25l4.72-4.72a.75.75 0 011.28.53v15.88a.75.75 0 01-1.28.53l-4.72-4.72H4.51c-.88 0-1.704-.507-1.938-1.354A9.01 9.01 0 012.25 12c0-.83.112-1.633.322-2.396C2.806 8.756 3.63 8.25 4.51 8.25H6.75z" />
        </svg>
      </div>
    </template>
    <template #dropdown>
      <div class="p-4 bg-white rounded-lg shadow-lg">
        <div class="flex items-center justify-between mb-2">
          <span class="text-sm font-medium text-gray-700">Volume</span>
          <span class="text-sm font-medium text-purple-600">{{ Math.round(volume * 100) }}%</span>
        </div>
        <input
          id="volume-slider"
          type="range"
          min="0"
          max="1"
          step="0.01"
          v-model="volume"
          class="w-full h-2 bg-gray-200 rounded-lg appearance-none cursor-pointer accent-purple-500 hover:accent-purple-600 focus:outline-none focus:ring-2 focus:ring-purple-500"
        />
      </div>
    </template>
  </Dropdown>
</template>
