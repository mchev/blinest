<p>Il s'agit plus particulièrement des tiers suivants&nbsp;:</p>
<p><span id="span_id_tiers_cookies">- Google Analytics</span> </p>
<p>De plus, le site intègre des boutons de réseaux sociaux, permettant à l'utilisateur de partager son activité sur le site. Des fichiers "cookies" de ces réseaux sociaux sont par conséquent susceptibles d'être stockés sur l'ordinateur de l'utilisateur lorsqu'il utilise ces fonctionnalités.</p>
<p>L'attention de l'utilisateur est portée sur le fait que ces sites disposent de politiques de confidentialité propres et de conditions générales d'utilisation possiblement différentes du site. L'éditeur du site invite les utilisateurs à consulter les politiques de confidentialité et les conditions générales d'utilisation de ces sites.<br><br></p>

